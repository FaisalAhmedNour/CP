
#include<bits/stdc++.h>
using namespace std;

bool isValidSudoku(vector<vector<char>>& board)
{
    //cheak rows
    for(int i = 0; i < 9; i++)
    {
        int arr[10] = {0};
        for(int j = 0; j < 9; j++)
        {
            int x = board[i][j] - '0';
            if(x >= 1 && x <= 9)
            {
                arr[x]++;
                if(arr[x] > 1) return 0;
            }
        }
    }

    //cheak cols
    for(int i = 0; i < 9; i++)
    {
        int arr[10] = {0};
        for(int j = 0; j < 9; j++)
        {
            int x = board[j][i] - '0';
            if(x >= 1 && x <= 9)
            {
                arr[x]++;
                if(arr[x] > 1) return 0;
            }
        }
    }
    //cheak sub-boxes
    for(int i = 0; i < 9; i += 3)
    {
        for(int j = 0; j < 9; j += 3)
        {
            int arr[10] = {0};
            for(int k = i; k < i+3; k++)
            {
                for(int l = j; l < j+3; l++)
                {
                    int x = board[k][l] - '0';
                    if(x >= 1 && x <= 9)
                    {
                        arr[x]++;
                        if(arr[x] > 1) return 0;
                    }
                }
            }
        }
    }

    return 1;
}

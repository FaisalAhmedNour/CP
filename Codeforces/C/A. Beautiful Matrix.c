
#include<stdio.h>

int main()
{
    int a[5][5];
    int i, j;

    for(i = 0; i < 5; i++)
    {
        for(j = 0; j < 5; j++)
        {
            scanf("%d", &a[i][j]);
        }
    }

    if(a[1][2] == 1 || a[2][1] == 1 || a[2][3] == 1 || a[3][2] == 1)
    {
        printf("1\n");
    }
    else if(a[1][1] == 1 || a[1][3] == 1 || a[3][1] == 1 || a[3][3] == 1 || a[0][2] == 1 || a[2][0] == 1 || a[2][4] == 1 || a[4][2] == 1)
    {
        printf("2\n");
    }
    else if(a[0][1] == 1 || a[0][3] == 1 || a[4][1] == 1 || a[4][3] == 1 || a[1][0] == 1 || a[1][4] == 1 || a[3][0] == 1 || a[3][4] == 1)
    {
        printf("3\n");
    }
    else if(a[0][0] == 1 || a[0][4] == 1 || a[4][0] == 1 || a[4][4] == 1)
    {
        printf("4\n");
    }
    else
    {
        printf("0\n");
    }

    return 0;
}
